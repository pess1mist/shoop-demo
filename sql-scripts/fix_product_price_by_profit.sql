-- ====================================
-- 反推产品单价和成本价（基于主任计算表）
-- ====================================

-- 计算逻辑：
-- 单价 = 收入（万元）× 10000 ÷ 产量（吨）
-- 单位成本 = 成本（万元）× 10000 ÷ 产量（吨）

-- 2023 年数据反推
-- P01: 2048 万 ÷ 4027 吨 = 5086.17 元/吨，1632 万 ÷ 4027 吨 = 4053.14 元/吨
-- P02: 912 万 ÷ 2234 吨 = 4082.36 元/吨，684 万 ÷ 2234 吨 = 3062.67 元/吨
-- P03: 240 万 ÷ 1534 吨 = 1564.54 元/吨，234 万 ÷ 1534 吨 = 1525.42 元/吨

-- 2024 年数据反推
-- P01: 2304 万 ÷ 4491 吨 = 5130.26 元/吨，1848 万 ÷ 4491 吨 = 4115.12 元/吨
-- P02: 1026 万 ÷ 2453 吨 = 4183.45 元/吨，780 万 ÷ 2453 吨 = 3180.19 元/吨
-- P03: 250 万 ÷ 1681 吨 = 1487.21 元/吨，228 万 ÷ 1681 吨 = 1356.34 元/吨

-- 2025 年数据反推
-- P01: 2560 万 ÷ 4860 吨 = 5267.49 元/吨，2112 万 ÷ 4860 吨 = 4345.68 元/吨
-- P02: 1140 万 ÷ 2688 吨 = 4241.07 元/吨，874 万 ÷ 2688 吨 = 3251.49 元/吨
-- P03: 280 万 ÷ 1805 吨 = 1551.25 元/吨，250 万 ÷ 1805 吨 = 1385.04 元/吨

-- 由于 product 表只能存一个单价和成本价，我们取三年平均值
-- P01: (5086.17 + 5130.26 + 5267.49) ÷ 3 = 5161.31 元/吨，(4053.14 + 4115.12 + 4345.68) ÷ 3 = 4171.31 元/吨
-- P02: (4082.36 + 4183.45 + 4241.07) ÷ 3 = 4168.96 元/吨，(3062.67 + 3180.19 + 3251.49) ÷ 3 = 3164.78 元/吨
-- P03: (1564.54 + 1487.21 + 1551.25) ÷ 3 = 1534.33 元/吨，(1525.42 + 1356.34 + 1385.04) ÷ 3 = 1422.27 元/吨

-- 更新 product 表
UPDATE product 
SET unit_price = 5161.31, cost_price = 4171.31 
WHERE product_code = 'P01';

UPDATE product 
SET unit_price = 4168.96, cost_price = 3164.78 
WHERE product_code = 'P02';

UPDATE product 
SET unit_price = 1534.33, cost_price = 1422.27 
WHERE product_code = 'P03';

-- 验证更新结果
SELECT 
    product_code,
    product_name,
    unit_price AS '单价 (元/吨)',
    cost_price AS '成本价 (元/吨)',
    (unit_price - cost_price) AS '单位利润 (元/吨)',
    ROUND((unit_price - cost_price) / unit_price * 100, 2) AS '利润率 (%)'
FROM product
ORDER BY product_code;
