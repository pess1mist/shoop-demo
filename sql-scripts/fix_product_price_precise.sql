-- ====================================
-- 精确反推产品单价和成本价（基于主任计算表）
-- ====================================

-- 计算逻辑：
-- 综合单价 = 三年总收入  三年总产量
-- 综合单位成本 = 三年总成本 ÷ 三年总产量

-- P01 三年数据：
-- 总收入 = 6912 万，总产量 = 4027+4491+4860 = 13378 吨
-- 总成本 = 5592 万
-- 综合单价 = 6912 × 10000 ÷ 13378 = 5167.42 元/吨
-- 综合单位成本 = 5592 × 10000 ÷ 13378 = 4180.74 元/吨

-- P02 三年数据：
-- 总收入 = 3078 万，总产量 = 2234+2453+2688 = 7375 吨
-- 总成本 = 2338 万
-- 综合单价 = 3078 × 10000 ÷ 7375 = 4173.56 元/吨
-- 综合单位成本 = 2338 × 10000  7375 = 3170.85 元/吨

-- P03 三年数据：
-- 总收入 = 770 万，总产量 = 1534+1681+1805 = 5020 吨
-- 总成本 = 712 万
-- 综合单价 = 770 × 10000 ÷ 5020 = 1533.86 元/吨
-- 综合单位成本 = 712 × 10000 ÷ 5020 = 1418.33 元/吨

-- 更新 product 表（精确到小数点后 2 位）
UPDATE product 
SET unit_price = 5167.42, cost_price = 4180.74 
WHERE product_code = 'P01';

UPDATE product 
SET unit_price = 4173.56, cost_price = 3170.85 
WHERE product_code = 'P02';

UPDATE product 
SET unit_price = 1533.86, cost_price = 1418.33 
WHERE product_code = 'P03';

-- 验证更新结果
SELECT 
    product_code,
    product_name,
    unit_price AS '单价 (元/吨)',
    cost_price AS '成本价 (元/吨)',
    (unit_price - cost_price) AS '单位利润 (元/吨)',
    ROUND((unit_price - cost_price) / unit_price * 100, 2) AS '利润率 (%)'
FROM product
ORDER BY product_code;

